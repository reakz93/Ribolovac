public class Vodostaj {

    private String postaja;
    private String vodotok;
    private String datum;
    private String razina;

    public String getPostaja() {
        return postaja;
    }

    public void setPostaja(String postaja) {
        this.postaja = postaja;
    }

    public String getVodotok() {
        return vodotok;
    }

    public void setVodotok(String vodotok) {
        this.vodotok = vodotok;
    }

    public String getDatum() {
        return datum;
    }

    public void setDatum(String datum) {
        this.datum = datum;
    }

    public String getRazina() {
        return razina;
    }

    public void setRazina(String razina) {
        this.razina = razina;
    }
}
